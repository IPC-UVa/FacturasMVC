    private void mesSpinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_mesSpinnerStateChanged
        // TODO add your handling code here:
        int dia = (int) this.diaSpinner.getValue();
        int mes = (int) this.mesSpinner.getValue();
        SpinnerModel diaModel = new SpinnerNumberModel(dia, 1, getDiaMaximo(mes), 1);
        this.diaSpinner.setModel(diaModel);
    }//GEN-LAST:event_mesSpinnerStateChanged

    private int getDiaMaximo(int mes) {
        switch (mes) {
            case 2:
                return 28;
            case 4:
                return 30;
            case 6:
                return 30;
            case 9:
                return 30;
            case 11:
                return 30;
            default:
                return 31;
        }
    }

    public void actualizarListaFacturas(ArrayList<Factura> listaFacturas) {
        DefaultListModel<String> modelo = new DefaultListModel<>();
        for (Factura factura : listaFacturas) {
            modelo.addElement(factura.toString());
        }
        facturasList.setModel(modelo);
    }

    

    public String getNombreFacturaSeleccionada() {
        return this.facturasList.getSelectedValue();
    }

    public void actualizarCamposFacturaExistente(Factura factura) {
        this.asuntoTextField.setText(factura.getAsunto());
        this.diaSpinner.setValue(factura.getFecha().getDayOfMonth());
        this.mesSpinner.setValue(factura.getFecha().getMonthValue());
        this.anoSpinner.setValue(factura.getFecha().getYear());
        this.tipoComboBox.setSelectedItem(factura.getTipo());
    }
    
